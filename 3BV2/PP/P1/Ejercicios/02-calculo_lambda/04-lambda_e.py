# Define una función lambda llamada 'starts_with' que verifica si una cadena comienza con 'P'
# Si la cadena comienza con 'P', la función lambda devuelve True, de lo contrario devuelve False
starts_with = lambda x: True if x.startswith('P') else False
print(starts_with('Python'))  # Imprime el resultado de la función 'starts_with' con el argumento 'Python'

# Redefine la función lambda 'starts_with'
# Si la cadena comienza con 'P', la función lambda devuelve True, de lo contrario devuelve False
starts_with = lambda x: True if x.startswith('P') else False
print(starts_with('Java'))  # Imprime el resultado de la función 'starts_with' con el argumento 'Java'

